    // Función para manejar la visibilidad de los elementos dentro de cada galería
    function updateGalleryVisibility(gallery) {
        const items = gallery.querySelectorAll('.tutela-list li');
        let currentIndex = 0;

        const prevButton = gallery.querySelector('.arrow-left');
        const nextButton = gallery.querySelector('.arrow-right');

        function updateVisibility() {
            items.forEach((item, index) => {
                if (index === currentIndex) {
                    item.classList.add('visible');
                } else {
                    item.classList.remove('visible');
                }
            });

            prevButton.disabled = currentIndex === 0;
            nextButton.disabled = currentIndex === items.length - 1;
        }

        prevButton.addEventListener('click', () => {
            if (currentIndex > 0) {
                currentIndex--;
                updateVisibility();
            }
        });

        nextButton.addEventListener('click', () => {
            if (currentIndex < items.length - 1) {
                currentIndex++;
                updateVisibility();
            }
        });

        // Selecciona todos los elementos <h3> dentro de la lista de cada galería
        const h3Elements = gallery.querySelectorAll('.tutela-list li h3');

        h3Elements.forEach(h3 => {
            h3.addEventListener('click', () => {
                const pContainer = h3.nextElementSibling;

                if (pContainer.classList.contains('hidden')) {
                    pContainer.classList.remove('hidden');
                } else {
                    pContainer.classList.add('hidden');
                }
            });
        });

        updateVisibility(); // Actualiza la visibilidad al cargar la página
    }

