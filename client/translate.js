function toggleButtons(resultId) {
    var resultElement = document.getElementById(resultId);
    var buttons = resultElement.querySelector('.buttons');
    var hoverText = resultElement.querySelector('.hover-text');

    // Mostrar u ocultar el hover-text con animación vertical
    if (hoverText.style.display === 'block') {
        // Ocultar el hover-text
        hoverText.style.height = '0';
        hoverText.style.opacity = '0';
        setTimeout(() => {
            hoverText.style.display = 'none';
        }, 300); // Tiempo debe coincidir con la duración de la animación
    } else {
        // Mostrar el hover-text
        hoverText.style.display = 'block';
        hoverText.style.height = hoverText.scrollHeight + 'px';
        hoverText.style.opacity = '1';
    }

    // Alternar la visibilidad de los botones
    buttons.classList.toggle('show');
}



